def set_all_seeds(seed: int = 42):
    os.environ["PYTHONHASHSEED"] = str(seed)
    random.seed(seed)
    np.random.seed(seed)
    tf.random.set_seed(seed)


def nasa_score_func(y_true, y_pred):
    """
    NASA scoring (early predictions cheaper than late).
    d = y_pred - y_true
      if d < 0 (predict smaller RUL = early):  exp(-d/13) - 1
      else (predict larger RUL = late):        exp(d/10) - 1
    """
    d = y_pred - y_true
    scores = np.where(d < 0,
                      np.exp(-d / 13.0) - 1.0,
                      np.exp(d / 10.0) - 1.0)
    return float(np.sum(scores))


def evaluate_metrics(y_true, y_pred):
    y_true = y_true.flatten().astype(np.float32)
    y_pred = y_pred.flatten().astype(np.float32)
    rmse = float(np.sqrt(mean_squared_error(y_true, y_pred)))
    nasa = nasa_score_func(y_true, y_pred)
    return rmse, nasa


class TrainingTracker(keras.callbacks.Callback):
    """Pretty training log for ATCN."""
    def on_train_begin(self, logs=None):
        print("\n🚀 ATCN training started")

    def on_epoch_begin(self, epoch, logs=None):
        print(f"\n— Epoch {epoch + 1}/{self.params['epochs']} —")

    def on_epoch_end(self, epoch, logs=None):
        loss = logs.get("loss", float("nan"))
        val_loss = logs.get("val_loss", float("nan"))
        try:
            lr = float(tf.keras.backend.get_value(self.model.optimizer.learning_rate))
        except Exception:
            lr = float("nan")
        print(f"   loss={loss:.5f} | val_loss={val_loss:.5f} | lr={lr:.6f}")

    def on_train_end(self, logs=None):
        print("🏁 ATCN training finished.\n")


