def plot_real_vs_synth(X_real, X_syn, feature_cols, out_path, n_sensors=3):
    """
    Compare real vs synthetic trajectories for a few sensors.
    """
    plt.figure(figsize=(10, 4 * n_sensors))

    idx = np.random.randint(0, X_real.shape[0])
    real_seq = X_real[idx]
    syn_seq  = X_syn[idx]

    timesteps = np.arange(real_seq.shape[0])
    num_plots = min(n_sensors, real_seq.shape[1])

    for i in range(num_plots):
        plt.subplot(num_plots, 1, i+1)
        plt.plot(timesteps, real_seq[:, i], label="Real", linewidth=2)
        plt.plot(timesteps, syn_seq[:, i], label="Synthetic", linestyle="--")
        sensor_name = feature_cols[i] if i < len(feature_cols) else f"sensor_{i+1}"
        plt.title(f"Real vs Synthetic – {sensor_name}")
        plt.xlabel("Time step in window")
        plt.ylabel("Scaled value")
        plt.legend()

    plt.tight_layout()
    plt.savefig(out_path, dpi=220)
    plt.close()
    print(f"[Saved] {out_path}")


