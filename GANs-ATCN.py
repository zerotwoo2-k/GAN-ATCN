def load_fd(data_path=DATA_PATH, fd=FDX):
    """Load FDxxx train/test/RUL with standard C-MAPSS columns."""
    cols = (
        ["unit_nr", "cycle"]
        + [f"setting_{i}" for i in range(1, 4)]
        + [f"sensor_{i}" for i in range(1, 22)]
    )

    train_file = os.path.join(data_path, f"train_{fd}.txt")
    test_file  = os.path.join(data_path, f"test_{fd}.txt")
    rul_file   = os.path.join(data_path, f"RUL_{fd}.txt")

    if not (os.path.exists(train_file) and os.path.exists(test_file) and os.path.exists(rul_file)):
        raise FileNotFoundError(
            f"{fd} files not found under ./data.\n"
            f"Expected train_{fd}.txt, test_{fd}.txt, RUL_{fd}.txt"
        )

    train_df = pd.read_csv(train_file, sep=r"\s+", header=None, names=cols)
    test_df  = pd.read_csv(test_file,  sep=r"\s+", header=None, names=cols)
    rul_df   = pd.read_csv(rul_file,   sep=r"\s+", header=None, names=["RUL"])
    return train_df, test_df, rul_df


def add_rul(train_df, cap=RUL_CAP):
    """Add piecewise-capped RUL for each training unit."""
    df = train_df.copy()
    max_cycles = df.groupby("unit_nr")["cycle"].max()
    df = df.merge(max_cycles.to_frame("max_cycle"), left_on="unit_nr", right_index=True)
    df["RUL"] = (df["max_cycle"] - df["cycle"]).clip(upper=cap).astype(np.float32)
    df.drop(columns=["max_cycle"], inplace=True)
    return df


