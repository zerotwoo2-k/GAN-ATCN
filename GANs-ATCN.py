"""
cGAN + ATCN augmentation on C-MAPSS (FD001–FD004)

Goal:
  Scenario 3 — show that ATCN can still perform well when trained on a mix of
  limited real data + synthetic data generated by a lightweight conditional GAN.

Pipeline:
  1) Load FD00X and preprocess (RUL labels + curated sensors)
  2) Simulate LOW-DATA scenario (subset of units, e.g. 30%)
  3) Scale features on small-train only (MinMax [-1,1])
  4) Train conditional GAN (G, D) on (X_small_train_scaled, y_small_train)
  5) Generate synthetic sequences conditioned on RUL
  6) Train:
       - ATCN_real_small         on small real only
       - ATCN_real_plus_syn      on real small + synthetic
  7) Evaluate both on REAL test set
  8) Save models, CSV summary, and plots:
       - real vs synthetic sequences (few sensors)
       - prediction vs truth
       - scatter + residuals

Expected layout:
  <project>/
    gan_atcn_augmentation.py
    data/
      train_FD001.txt
      test_FD001.txt
      RUL_FD001.txt
      (and optionally FD002–FD004)
"""

