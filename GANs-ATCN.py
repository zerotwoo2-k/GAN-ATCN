def create_windows_train(df, seq_len, feature_cols):
    """Create sliding windows and labels for train/val."""
    X_list, y_list, units_list = [], [], []
    for u in df["unit_nr"].unique():
        u_df = df[df["unit_nr"] == u].sort_values("cycle")
        values = u_df[feature_cols].values.astype(np.float32)
        rul    = u_df["RUL"].values.astype(np.float32)

        if len(values) < seq_len:
            continue

        for i in range(len(values) - seq_len + 1):
            X_list.append(values[i:i + seq_len])
            y_list.append(rul[i + seq_len - 1])
            units_list.append(u)

    X = np.array(X_list, dtype=np.float32)
    y = np.array(y_list, dtype=np.float32)
    units = np.array(units_list)
    return X, y, units


def create_windows_test(test_df, rul_df, seq_len, feature_cols):
    """One window per test unit: last seq_len cycles, left-padded if needed."""
    X_list = []
    for u in test_df["unit_nr"].unique():
        u_df = test_df[test_df["unit_nr"] == u].sort_values("cycle")
        values = u_df[feature_cols].values.astype(np.float32)

        if len(values) >= seq_len:
            w = values[-seq_len:]
        else:
            pad_len = seq_len - len(values)
            pad = np.repeat(values[:1], pad_len, axis=0)
            w = np.vstack([pad, values])
        X_list.append(w)

    X_test = np.array(X_list, dtype=np.float32)
    y_test = rul_df["RUL"].values.astype(np.float32)
    return X_test, y_test


def preprocess_fd(fd=FDX):
    """
    Load FDxxx, assign RUL, select sensors, and build base windows.
    Scaling is done later on the LOW-DATA split.
    """
    train_df, test_df, rul_df = load_fd(DATA_PATH, fd=fd)
    train_df = add_rul(train_df, cap=RUL_CAP)
    feature_cols = get_useful_sensors(train_df, fd=fd)

    print(f"\n=== PREPROCESSING {fd} ===")
    print("Using features:", feature_cols)

    X_all, y_all, units_all = create_windows_train(train_df, SEQ_LEN, feature_cols)
    X_test, y_test = create_windows_test(test_df, rul_df, SEQ_LEN, feature_cols)

    print(f"Train windows: {X_all.shape}, Test windows: {X_test.shape}")
    return train_df, X_all, y_all, units_all, X_test, y_test, feature_cols


def make_low_data_split(X_all, y_all, units_all,
                        frac_units=LOW_DATA_FRACTION_UNITS,
                        val_frac_units=VAL_FRACTION_UNITS,
                        seed=42):
    """
    Simulate low-data scenario:
      - pick subset of units (fraction of all units)
      - within those, choose some units for validation
    """
    rng = np.random.RandomState(seed)
    unique_units = np.unique(units_all)

    n_low = max(1, int(np.ceil(frac_units * len(unique_units))))
    low_units = rng.choice(unique_units, size=n_low, replace=False)

    mask_low = np.isin(units_all, low_units)
    X_low = X_all[mask_low]
    y_low = y_all[mask_low]
    units_low = units_all[mask_low]

    unique_low_units = np.unique(units_low)
    n_val = max(1, int(np.ceil(val_frac_units * len(unique_low_units))))
    val_units = rng.choice(unique_low_units, size=n_val, replace=False)
    train_units = np.array([u for u in unique_low_units if u not in val_units])

    train_mask = np.isin(units_low, train_units)
    val_mask   = np.isin(units_low, val_units)

    X_tr = X_low[train_mask]
    y_tr = y_low[train_mask]
    X_val = X_low[val_mask]
    y_val = y_low[val_mask]

    print(f"\n📉 LOW-DATA SCENARIO:")
    print(f"  total units: {len(unique_units)}")
    print(f"  low-data units: {len(low_units)} "
          f"(train={len(train_units)}, val={len(val_units)})")
    print(f"  X_tr: {X_tr.shape}, X_val: {X_val.shape}")

    return X_tr, y_tr, X_val, y_val


# ===================== 4. ATCN MODEL =====================

class ISABlock(layers.Layer):
    """Improved self-attention block (simple version)."""
    def __init__(self, d=64, heads=2):
        super().__init__()
        self.q = layers.Conv1D(d, 3, padding="same")
        self.k = layers.Conv1D(d, 3, padding="same")
        self.v = layers.Conv1D(d, 3, padding="same")
        self.mha = layers.MultiHeadAttention(heads, key_dim=d // heads)
        self.n1 = layers.LayerNormalization()
        self.ff = keras.Sequential([
            layers.Dense(d * 2, activation="relu"),
            layers.Dense(d)
        ])
        self.n2 = layers.LayerNormalization()

    def call(self, x):
        q, k, v = self.q(x), self.k(x), self.v(x)
        att = self.mha(q, k, v)
        x1 = self.n1(x + att)
        ff = self.ff(x1)
        return self.n2(x1 + ff)


class TCNBlock(layers.Layer):
    def __init__(self, f=64, k=3, d=1):
        super().__init__()
        self.c1 = layers.Conv1D(f, k, padding="causal",
                                dilation_rate=d, activation="relu")
        self.c2 = layers.Conv1D(f, k, padding="causal",
                                dilation_rate=d, activation="relu")
        self.s  = layers.Conv1D(f, 1)

    def call(self, x):
        y = self.c1(x)
        y = self.c2(y)
        return y + self.s(x)


def se_block(x):
    f = x.shape[-1]
    s = layers.GlobalAveragePooling1D()(x)
    s = layers.Dense(max(1, f // 8), activation="relu")(s)
    s = layers.Dense(f, activation="sigmoid")(s)
    s = layers.Reshape((1, f))(s)
    return layers.Multiply()([x, s])


def build_atcn_model(input_shape):
    """
    ATCN:
      - Dense projection to d_model
      - ISA (improved self-attention)
      - 4× TCN blocks with dilations [1,2,4,8]
      - SE channel attention
      - GAP + dense head
    """
    inp = layers.Input(shape=input_shape)
    x = layers.Dense(ATCN_DMODEL)(inp)
    x = ISABlock(ATCN_DMODEL, 2)(x)
    for d in [1, 2, 4, 8]:
        x = TCNBlock(ATCN_FILTERS, 3, d)(x)
    x = se_block(x)
    x = layers.GlobalAveragePooling1D()(x)
    x = layers.Dense(64, activation="relu")(x)
    out = layers.Dense(1)(x)
    model = keras.Model(inp, out, name="ATCN")
    return model


def train_atcn(X_tr, y_tr, X_val, y_val, X_test, y_test, tag: str):
    """
    Train ATCN and evaluate on real test set.
    Saves model as atcn_{tag}_{FDX}.h5
    """
    model = build_atcn_model((SEQ_LEN, X_tr.shape[-1]))
    model.compile(
        optimizer=keras.optimizers.Adam(1e-3),
        loss=keras.losses.Huber(delta=5.0)
    )

    ckpt_path = os.path.join(RESULTS_DIR, f"atcn_{tag}_{FDX}.h5")

    callbacks = [
        TrainingTracker(),
        keras.callbacks.EarlyStopping(
            monitor="val_loss",
            patience=8,
            restore_best_weights=True
        ),
        keras.callbacks.ReduceLROnPlateau(
            monitor="val_loss",
            factor=0.5,
            patience=4,
            min_lr=1e-6
        ),
        keras.callbacks.ModelCheckpoint(
            ckpt_path,
            monitor="val_loss",
            save_best_only=True,
            save_weights_only=False
        )
    ]

    history = model.fit(
        X_tr, y_tr,
        validation_data=(X_val, y_val),
        epochs=ATCN_EPOCHS,
        batch_size=ATCN_BATCH,
        verbose=0,
        callbacks=callbacks
    )

    y_pred = model.predict(X_test, batch_size=ATCN_BATCH, verbose=0).flatten()
    rmse, nasa = evaluate_metrics(y_test, y_pred)

    print(f"\n✅ ATCN ({tag}) → Test RMSE={rmse:.3f}, NASA={nasa:.1f}")
    print(f"   Saved model → {ckpt_path}")

    return model, history, y_pred, rmse, nasa


# ===================== 5. CONDITIONAL GAN (cGAN) =====================

def build_generator(seq_len, n_features, noise_dim=NOISE_DIM):
    """
    Conditional generator G(z, y) → sequence (seq_len, n_features)
    Condition is scalar RUL (unscaled).
    Data is expected to be scaled to [-1,1], so we use tanh at the end.
    """
    z_in = layers.Input(shape=(noise_dim,), name="z_input")
    y_in = layers.Input(shape=(1,), name="y_input")

    x = layers.Concatenate()([z_in, y_in])           # (batch, noise_dim+1)
    x = layers.Dense(128, activation="relu")(x)
    x = layers.Dense(seq_len * 64, activation="relu")(x)
    x = layers.Reshape((seq_len, 64))(x)

    y_rep = layers.RepeatVector(seq_len)(y_in)       # (batch, seq_len, 1)
    x = layers.Concatenate(axis=-1)([x, y_rep])      # (batch, seq_len, 65)

    x = layers.Conv1D(64, 3, padding="same", activation="relu")(x)
    x = layers.Conv1D(32, 3, padding="same", activation="relu")(x)

    out = layers.Conv1D(n_features, 1, padding="same", activation="tanh")(x)

    model = keras.Model([z_in, y_in], out, name="Generator")
    return model


def build_discriminator(seq_len, n_features):
    """
    Conditional discriminator D(x, y) → probability(real)
    """
    x_in = layers.Input(shape=(seq_len, n_features), name="x_input")
    y_in = layers.Input(shape=(1,), name="y_input")

    y_rep = layers.RepeatVector(seq_len)(y_in)
    d = layers.Concatenate(axis=-1)([x_in, y_rep])   # (batch, seq_len, n_features+1)

    d = layers.Conv1D(64, 3, padding="same", activation="relu")(d)
    d = layers.Conv1D(64, 3, padding="same", activation="relu")(d)
    d = layers.GlobalAveragePooling1D()(d)
    d = layers.Dense(64, activation="relu")(d)
    out = layers.Dense(1, activation="sigmoid")(d)

    model = keras.Model([x_in, y_in], out, name="Discriminator")
    return model


def train_gan(generator, discriminator, X_train, y_train,
              epochs=GAN_EPOCHS,
              batch_size=GAN_BATCH,
              noise_dim=NOISE_DIM):
    """
    Simple conditional GAN training loop.
    X_train: scaled to [-1,1], shape (N, seq_len, n_features)
    y_train: original RUL labels, shape (N,)
    """
    bce = keras.losses.BinaryCrossentropy(from_logits=False)
    g_opt = keras.optimizers.Adam(1e-4)
    d_opt = keras.optimizers.Adam(1e-4)

    dataset = tf.data.Dataset.from_tensor_slices(
        (X_train.astype("float32"), y_train.astype("float32"))
    ).shuffle(buffer_size=len(X_train)).batch(batch_size, drop_remainder=True)

    @tf.function
    def train_step(real_x, real_y):
        # real_y shape: (batch,), we need (batch,1)
        real_y_exp = tf.reshape(real_y, (-1, 1))

        # ----------------- Train Discriminator -----------------
        noise = tf.random.normal(shape=(tf.shape(real_x)[0], noise_dim))
        with tf.GradientTape() as d_tape:
            fake_x = generator([noise, real_y_exp], training=True)

            real_logits = discriminator([real_x, real_y_exp], training=True)
            fake_logits = discriminator([fake_x, real_y_exp], training=True)

            d_loss_real = bce(tf.ones_like(real_logits), real_logits)
            d_loss_fake = bce(tf.zeros_like(fake_logits), fake_logits)
            d_loss = d_loss_real + d_loss_fake

        d_grads = d_tape.gradient(d_loss, discriminator.trainable_variables)
        d_opt.apply_gradients(zip(d_grads, discriminator.trainable_variables))

        # ----------------- Train Generator -----------------
        noise = tf.random.normal(shape=(tf.shape(real_x)[0], noise_dim))
        with tf.GradientTape() as g_tape:
            fake_x = generator([noise, real_y_exp], training=True)
            fake_logits = discriminator([fake_x, real_y_exp], training=True)
            g_loss = bce(tf.ones_like(fake_logits), fake_logits)

        g_grads = g_tape.gradient(g_loss, generator.trainable_variables)
        g_opt.apply_gradients(zip(g_grads, generator.trainable_variables))

        return d_loss, g_loss

    print("\n🎭 Training conditional GAN (Generator + Discriminator)")
    for epoch in range(epochs):
        d_losses, g_losses = [], []
        for real_x_batch, real_y_batch in dataset:
            d_loss, g_loss = train_step(real_x_batch, real_y_batch)
            d_losses.append(d_loss.numpy())
            g_losses.append(g_loss.numpy())
        print(f"Epoch {epoch+1}/{epochs} | D_loss={np.mean(d_losses):.4f} | "
              f"G_loss={np.mean(g_losses):.4f}")

    return generator, discriminator


def generate_synthetic_sequences(generator, y_train, n_samples, noise_dim=NOISE_DIM, batch_size=128):
    """
    Generate synthetic sequences conditioned on RUL labels.
    We re-use y_train as conditioning labels.
    """
    n_train = len(y_train)
    idx = np.random.randint(0, n_train, size=n_samples)
    y_cond = y_train[idx].astype(np.float32).reshape(-1, 1)

    synth_list = []
    for i in range(0, n_samples, batch_size):
        y_batch = y_cond[i:i+batch_size]
        z_batch = np.random.normal(size=(len(y_batch), noise_dim)).astype(np.float32)
        x_batch = generator.predict([z_batch, y_batch], verbose=0)
        synth_list.append(x_batch)

    X_syn = np.concatenate(synth_list, axis=0)
    y_syn = y_cond.flatten().astype(np.float32)
    return X_syn, y_syn


